# Write the necessary code calculate the volume and surface area
# of a cylinder with a radius of 3.14 and a height of 5.
# Print out the result.
radius = 3.14
height = 5
volume_cyl = 3.14 * radius**2 * height
area_cyl = 2 * 3.14 * radius**2 + 2 * 3.14 * radius * height
print("The volume is", volume_cyl, "and the area is", area_cyl)

